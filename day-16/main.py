from turtle import Turtle, Screen

bob = Turtle()
bob.shape("turtle")
bob.shapesize(2.0, 2.0, 1)
bob.color("chartreuse")
bob.forward(100)

my_screen = Screen()
print(my_screen.canvheight)
my_screen.exitonclick()

